class Casting{
    public static void main(String[] args) {
        double a;
        int b;
        a = 10.23;

        System.out.println("\n Value of A [double] is "+a);
        System.out.println("\n converting double to integer");

        b = (int)a;

        System.out.println("\n Value of B [integer] is "+b);
        System.out.println("\n");

        int c;
        double d;
        c = 79;

        System.out.println("\n Value of C [integer] is "+c);
        System.out.println("\n converting integer to double");

        d = (double)c;

        System.out.println("\n Value of D [double] is "+d);
        System.out.println("\n");



    }
}